#include <stdio.h>

int	ft_sqrt(int nb)
{
	int	n;

	n = 0;
	if (nb < 0)
		return (0);
	while (n <= 46340)
	{
		if (n * n == nb)
			return (n);
		n++;
	}
	return (0);
}
