#include <unistd.h>

int	ft_is_prime(int nb)
{
	int	i;
	int	j;

	j = 0;
	i = 1;
	if (nb <= 1)
		return (0);
	while (i < nb && j < 3)
	{
		if (nb % i == 0)
			j++;
		i++;
	}
	if (j > 2)
		return (0);
	else
		return (1);
}
