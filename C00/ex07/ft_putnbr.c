#include <unistd.h>

void	print(char c)
{
	write(1, &c, 1);
}

void	ft_put_nbr(int nb)
{	
	if (nb < 0)
	{
		nb = nb * -1;
		print('-');
	}
	if (nb < '9')
	{
		print(nb / 10 + 48);
		print(nb % 10 + 48);
	}
}
