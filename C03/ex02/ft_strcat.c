#include <stdio.h>

char	*ft_strcat(char *dest, char *src)
{
	char	*i;
	char	*j;

	i = src;
	j = dest;
	while (*j != '\0')
	{
		j++;
	}
	while (*i != '\0')
	{
		*j = *i;
		i++;
		j++;
	}
	*j = '\0';
	return (dest);
}
