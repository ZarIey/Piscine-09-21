#include <unistd.h>

char	*ft_strcapitalize(char *str)
{
	char	*i;

	i = str;
	while (*i != '\0')
	{
		if (*i <= 97 && *i >= 122)
		{
			i++;
		}
		else if (*i >= 97 && *i <= 122)
		{
			*i = *i - 32;
			i++;
			while (*i <= 97 && *i >= 122)
				i++;
		}
		i++;
	}
	return (str);
}
