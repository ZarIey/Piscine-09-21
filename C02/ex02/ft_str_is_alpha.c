#include <unistd.h>

int	ft_str_is_alpha(char *str)
{
	char	*i;

	i = str;
	while (*i != '\0')
	{
		if ((*i >= 'A' && *i <= 'Z') || (*i >= 'a' && *i <= 'z'))
			i++;
		else
			return (0);
	}
	return (1);
}
