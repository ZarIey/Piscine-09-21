#include <unistd.h>

int	ft_str_is_printable(char *str)
{
	char	*i;

	i = str;
	while (*i != '\0')
	{
		if (*i >= 32 && *i <= 126)
			i++;
		else
			return (0);
	}
	return (1);
}
