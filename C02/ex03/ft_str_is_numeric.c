#include <unistd.h>

int	ft_str_is_numeric(char *str)
{
	char	*i;

	i = str;
	while (*i != '\0')
	{
		if (*i >= 48 && *i <= 57)
			i++;
		else
			return (0);
	}
	return (1);
}
