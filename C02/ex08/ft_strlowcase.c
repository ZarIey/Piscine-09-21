#include <unistd.h>

char	*ft_strlowcase(char *str)
{
	char	*i;

	i = str;
	while (*i != '\0')
	{
		if (*i >= 65 && *i <= 90)
		{
			*i = *i + 32;
			i++;
		}
		else
			i++;
	}
	return (str);
}
